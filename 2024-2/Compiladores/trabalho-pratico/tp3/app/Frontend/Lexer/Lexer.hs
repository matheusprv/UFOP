showLexer :: String -> IO ()
showLexer input = do
    case lexer input of
        Left err -> putStrLn $ "Lexer error: " ++ err
        Right tokens -> mapM_ (putStrLn . showLexeme) tokens
  where
    showLexeme (Token _ lexeme) = show lexeme
